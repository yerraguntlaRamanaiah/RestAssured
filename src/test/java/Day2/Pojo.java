package Day2;

public class Pojo {
	
	String name;
	String location;
	String cousesArr[];
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String[] getCousesArr() {
		return cousesArr;
	}
	public void setCousesArr(String[] cousesArr) {
		this.cousesArr = cousesArr;
	}
	
	

}
